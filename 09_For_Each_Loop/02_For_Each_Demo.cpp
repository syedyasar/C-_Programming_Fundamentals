// for .. each  loop  demo  program
#include <iostream>
using  namespace std;
main()
{
	float  a[] = {10.8 , 20.6 , 15.2 , 18.3};
	for(float  x :  a)  // x  is  each  element  of  array  'a'
			cout << x << "\n";  // 10.8  20.6  15.2   18.3
}

